package com.yahoo.rahul.moviesdoughnut.api.response.model;

public class Cast {
    int cast_id;
    String character;
    String credit_id;
    int gender;
    int id;
    String name;
    int order;
    String profile_path;

    public int getCast_id() {
        return cast_id;
    }

    public String getCharacter() {
        return character;
    }

    public String getCredit_id() {
        return credit_id;
    }

    public int getGender() {
        return gender;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getOrder() {
        return order;
    }

    public String getProfile_path() {
        return profile_path;
    }
}

