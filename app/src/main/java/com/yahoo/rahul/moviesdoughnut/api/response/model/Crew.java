package com.yahoo.rahul.moviesdoughnut.api.response.model;

public class Crew {
    String credit_id;
    String department;
    int gender;
    int id;
    String job;
    String name;
    String profile_path;

    public String getCredit_id() {
        return credit_id;
    }

    public String getDepartment() {
        return department;
    }

    public int getGender() {
        return gender;
    }

    public int getId() {
        return id;
    }

    public String getJob() {
        return job;
    }

    public String getName() {
        return name;
    }

    public String getProfile_path() {
        return profile_path;
    }
}

