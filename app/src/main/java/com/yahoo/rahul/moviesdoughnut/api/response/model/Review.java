package com.yahoo.rahul.moviesdoughnut.api.response.model;

public class Review {
    String id;
    String author;
    String content;
    String url;

    public String getId() {
        return id;
    }

    public String getAuthor() {
        return author;
    }

    public String getContent() {
        return content;
    }

    public String getUrl() {
        return url;
    }
}
